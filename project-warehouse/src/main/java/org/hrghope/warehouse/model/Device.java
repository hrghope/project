package org.hrghope.warehouse.model;

/**
 * 设备
 * 
 * @author ronggang.huangrg
 * 
 */
public class Device {

	/**
	 * 设备码
	 */
	private String code;

	/**
	 * 设备类型
	 */
	private String type;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
